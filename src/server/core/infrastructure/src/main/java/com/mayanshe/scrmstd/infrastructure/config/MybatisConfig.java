package com.mayanshe.scrmstd.infrastructure.config;

import com.mayanshe.scrmstd.infrastructure.support.JsonTypeHandler;
import org.mybatis.spring.annotation.MapperScan;
import org.mybatis.spring.boot.autoconfigure.ConfigurationCustomizer;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.Map;

@Configuration
@MapperScan("com.mayanshe.scrmstd.infrastructure.persistence.mapper")
public class MybatisConfig {
    @Bean
    public ConfigurationCustomizer configurationCustomizer() {
        return configuration -> configuration.getTypeHandlerRegistry()
                .register(Map.class, new JsonTypeHandler<>(Map.class));
    }
}
